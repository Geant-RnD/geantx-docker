#!/bin/bash -le

#------------------------------------------------------------------------------#
# make sure all startup files are sourced
#------------------------------------------------------------------------------#

if j in profile bashrc
do
    if [ -d /etc/${j}.d ]; then
        for i in /etc/${j}.d/*.sh
        do
            if [ -f ${i} ]; then source ${i}; fi
        done
    fi
done

#------------------------------------------------------------------------------#
#   run command
#------------------------------------------------------------------------------#
if [ -z "${1}" ]; then
    exec /bin/bash
fi

eval $@
